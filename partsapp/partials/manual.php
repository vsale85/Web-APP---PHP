    <h2>"Pretraga"</h2>
    <p>*Stranica namenjena za pretragu svega unetog u bazu, po raznim uslovima koje sami odredjujete</p><br>
    <p>*NAPOMENA, pretraga se vrši isključivo po odabiru "Prodaja" ili "Nabavka Delova", pretraga ne može da se vrši po oba kriterijuma, odnosno i po prodaji i nabavci istovremeno!
        "Prodaja" je predefinisana opcija, što znači ako ništa ne odaberete, pretraga će se vršiti u "Prodaja"!
    </p>

    <p>* Pretraga je moguća po sledećim uslovima:</p>
    <p>-prikaz svih unosa za "Prodaja"</p>
    <p>-prikaz svih unosa za "Nabavka Delova"</p>
    <p>-zatim po datumu OD i DO</p>
    <p>-po unetoj reči- striktno</p>
    <p>-rekord sadrži traženu reč</p>
    <p>-rekord počinje na traženu reč</p>
    <br>
    <h4>Pretraga po sadržaju tražene reči</h4>
    <p>-u polje za unos, unosite traženu reč, npr. "kvačilo"</p>
    <p>-zatim birate kriterijum "Prodaja" ili "Nabavka Delova"</p>
    <p>-polja za čekiranje NE čekirate</p>
    <p>-datum OD- birate od kog datuma, ili ostavljate prazno ukoliko želite pretragu svega</p>
    <p>-datum DO- birate datum Do kog želite da se pretraga izvrši, predefinisani datum je trenutni("današnji")</p>
    <p>-kliknete na dugme "GO" koje se nalazi uz polje za unos</p>
    <p>-rezultat pretrage u ovom slučaju će biti svi unosi koji u bilo kom polju sadrže reč "kvačilo", odnosno, ako se ta reč nalazi u jednom unosu u polju naziva, u drugom
        unosu u polju napomene kao na primer "korpa za kvačilo za Fiat punto", i u trećem unosu u polju napomene kao npr. "korpa sa kvačiloM", kao rezultat će se pojaviti sva tri rekorda
    </p>
    <br>
    <h4>Pretraga po- rekord(unos) počinje na traženu reč</h4>
    <p>-prikazuju se svi rekordi koji počinju na traženu reč i posle nje mogu imati 0, 1 ili više karaktera ili reči</p>
    <p>-u polje za unos, unosite traženu reč, npr "kvačilo"</p>
    <p>-zatim birate kriterijum "Prodaja" ili "Nabavka Delova"</p>
    <p>-ČEKIRAJTE polje "Pretraga počinje na reč..."</p>
    <p>-datum OD- birate od kog datuma, ili ostavljate prazno ukoliko želite pretragu svega</p>
    <p>-datum DO- birate datum Do kog želite da se pretraga izvrši, predefinisani datum je trenutni("današnji")</p>
    <p>-kliknete na dugme "GO" koje se nalazi uz polje za unos</p>
    <p>-rezultat pretrage u ovom slučaju će biti svi unosi koji POČINJU na reč "kvačilo", odnosno, ako se ta reč nalazi u jednom unosu u polju naziva, u drugom
        unosu u polju napomene kao na primer "korpa za kvačilo za Fiat punto", i u trećem unosu u polju napomene kao npr. "korpa sa kvačiloM", kao rezultat će se pojaviti SAMO prvi rekord, jer samo u polju naziva POČINJE na zadatu reč
    </p>
