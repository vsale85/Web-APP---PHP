    <h2>"Prodaja"</h2>
    <p>*stranica namenjena za evidenciju prodatih delova, opreme</p><br>
    <p>Prva stranica po logovanju na koju dodjete je stranica "Prodaja", u kojoj unosite prodate delove.</p>
    <h4>-polje "Datum" prodaje</h4>
    <p> -predefinisan datum je "današnji"</p>
    <p> -klikom na polje ili ikonicu kalendara(u zavisnosti od vašeg browser-a) otvara se kalendar za odabir datuma</p>
    <p> -NAPOMENA: format datuma zavisi od regionalnog i vremenskog podešavanja vašeg uredjaja</p>
    <p> -nakon odabranog datuma prodaje, prelazite na sledeće polje</p>
    <h4>-polje "Naziv Artikla"</h4>
    <p>-upisujete naziv po Vašem izboru</p>
    <p>(preporuka je da koristite identičan naziv za svaku vrstu artikla, radi kasnije preciznije pretrage)</p>
    <h4>-polje "Cena"</h4>
    <p>-unosite prodajnu cenu artikla</p>
    <h4>-polje "Model"</h4>
    <p>-unosite odgovarajući model dela/vozila(preporuka je da se koriste identični nazivi, radi kasnije preciznije pretrage)</p>
    <h4>-polje "Status Prodaje"</h4>
    <p>-predefinisana opcija je "Plaćeno", klikom na polje otvara se padajuci meni gde birate neku od dodatnih opcija, "Nije Plaćeno" ili "Ostalo"</p>
    <h4>-polje "Napomena"</h4>
    <p>- u polje napomene unosite sve ono što biste dodali kao opis same prodaje ili u slučaju da je kod polja "Status Prodaje"
        odabrano OSTALO, ovde možete opisati da li je delimično plaćeno ili kako god Vama odgovara, polje je ograničeno na 1000 karaktera</p>
    <p>Na kraju birate opciju "SAČUVAJ", ukoliko želite da sačuvate</p>
    <p>-nakon klika na "SAČUVAJ", ostajete na istoj stranici, sva polja za unos se brišu, i kao znak da je sačuvano pojavljuje se "sličica čovečuljka sa znakom odobravanja" na tri sekunde</p>
    <p>U slučaju da ipak NE želite sačuvati, klikom na "PONIŠTI", unos se NE čuva, ostajete na istoj stranici, i sva polja za unos se brišu</p>
